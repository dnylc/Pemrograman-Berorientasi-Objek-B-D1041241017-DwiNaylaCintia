package Pertemuan7;

public class LatihanDeretGanjil {
     public static void main(String[] args){
        int angka = 1; // Inisialisasi angka dengan nilai awal 1
        System.out.println("=== Bilangan Ganjil 1-15 ===");

        // Menggunakan while loop untuk mencetak bilangan ganjil dari 1 hingga 15
        while (angka <= 15) {
            System.out.print(angka + " "); // Mencetak angka ganjil saat ini diikuti oleh spasi

            angka += 2; // Menambahkan 2 untuk mendapatkan bilangan ganjil berikutnya
        } System.out.println();
    }

}

